    public void rellenar() {
        for (int i = 0; i < botones.length; i++) {
            for (int j = 0; j < botones[i].length; j++) {
                botones[i][j].setBackground(color1);
                botones[i][j].setForeground(color2);
            }
        }
    }

    public void calcular() {
        int rule = CmbRules.getSelectedIndex();

        if (rule == 0) {
            Rule90();
        setTitle("Rule 90");
        } else if (rule == 1) {
            Rule184();
        setTitle("Rule 184");
        }
    }

    public void Rule90() {
        for (int i = 1; i < botones.length; i++) {
            for (int j = 0; j < botones[i].length; j++) {
                int izquierda = getValorSeguro(i - 1, j - 1);
                int derecha = getValorSeguro(i - 1, j + 1);

                if (izquierda == derecha) {
                    valor[i][j] = 0;
                    botones[i][j].setBackground(color1);
                } else {
                    valor[i][j] = 1;
                    botones[i][j].setBackground(color2);
                }
            }
        }
    }

    public void Rule184() {
        for (int i = 1; i < botones.length; i++) {
            for (int j = 0; j < botones[i].length; j++) {
                int izquierda = getValorSeguro(i - 1, j - 1);
                int centro = getValorSeguro(i - 1, j);
                int derecha = getValorSeguro(i - 1, j + 1);

                if (centro == 1) {
                    if (derecha == 0) {
                        valor[i][j] = 0;
                        botones[i][j].setBackground(color1);
                    } else {
                        valor[i][j] = 1;
                        botones[i][j].setBackground(color2);
                    }
                } else {
                    if (izquierda == 1) {
                        valor[i][j] = 1;
                        botones[i][j].setBackground(color2);
                    } else {
                        valor[i][j] = 0;
                        botones[i][j].setBackground(color1);
                    }
                }
            }
        }
    }

    private int getValorSeguro(int fila, int col) {
        if (fila < 0 || fila >= valor.length || col < 0 || col >= valor[0].length) {
            return 0;
        }
        return valor[fila][col];
    }


